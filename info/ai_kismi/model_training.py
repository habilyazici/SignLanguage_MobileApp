# --- 1. Custom Attention Layer (Aynı Kalıyor, Gayet Başarılı) ---
class SelfAttention(Layer):
    def __init__(self, **kwargs):
        super(SelfAttention, self).__init__(**kwargs)

    def build(self, input_shape):
        self.W = self.add_weight(name="att_weight", shape=(input_shape[-1], 1), initializer="normal")
        self.b = self.add_weight(name="att_bias", shape=(input_shape[1], 1), initializer="zeros")
        super(SelfAttention, self).build(input_shape)

    def call(self, x):
        et = tf.squeeze(tf.tanh(tf.matmul(x, self.W) + self.b), axis=-1)
        at = tf.nn.softmax(et)
        at = tf.expand_dims(at, axis=-1)
        output = x * at
        return tf.reduce_sum(output, axis=1)

# --- 2. Model İnşası (Pro Versiyon) ---
def build_model_pro(input_shape, num_classes):
    inputs = Input(shape=input_shape) # (60, 106)

    # Giriş Verisini Normalize Et (LayerNorm ile sarsıntıları engelle)
    x = LayerNormalization()(inputs)

    # 1. Bidirectional LSTM
    # recurrent_dropout=0 kullanımı TFLite uyumluluğunu ve GPU hızını artırır.
    x = Bidirectional(LSTM(128, return_sequences=True, recurrent_dropout=0))(x)
    x = BatchNormalization()(x) # Ağı her adımda hizaya sokar
    x = Dropout(0.4)(x) # Overfitting için dropout'u biraz artırdık

    # 2. Bidirectional LSTM
    x = Bidirectional(LSTM(64, return_sequences=True, recurrent_dropout=0))(x)
    x = BatchNormalization()(x)
    x = Dropout(0.4)(x)

    # Attention Mekanizması
    x = SelfAttention()(x)

    # Dense Katmanları (Aralara BatchNormalization ekleyerek 'öğrenme patlamasını' önlüyoruz)
    x = Dense(256, activation='relu')(x) # Kapasiteyi biraz artırdık
    x = BatchNormalization()(x)
    x = Dropout(0.3)(x)

    x = Dense(128, activation='relu')(x)
    x = BatchNormalization()(x)

    # Çıkış Katmanı
    outputs = Dense(num_classes, activation='softmax')(x)

    model = Model(inputs, outputs)
    return model

