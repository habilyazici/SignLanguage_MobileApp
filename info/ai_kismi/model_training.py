def normalize_landmarks_pro(X):
    """
    X: (Samples, 60, 106)
    Daha kararlı bir öğrenme için 'Z-Score Normalization' ve
    'Relative Scaling' ekliyoruz.
    """
    X_norm = X.copy()

    for i in range(X_norm.shape[0]):  # Her video
        for j in range(X_norm.shape[1]):  # Her kare

            # 1. Parçalara ayır
            rh = X_norm[i, j, 0:42]
            lh = X_norm[i, j, 42:84]
            pose = X_norm[i, j, 84:106]

            # 2. SAĞ EL HİZALAMA VE ÖLÇEKLENDİRME
            if not np.all(rh == 0):
                # Bileğe göre merkezle
                rh[0::2] -= rh[0]
                rh[1::2] -= rh[1]
                # Ölçeklendir (Elin büyüklüğünü standartlaştır)
                scale = np.max(np.abs(rh)) + 1e-6
                rh /= scale

            # 3. SOL EL HİZALAMA VE ÖLÇEKLENDİRME
            if not np.all(lh == 0):
                lh[0::2] -= lh[0]
                lh[1::2] -= lh[1]
                scale = np.max(np.abs(lh)) + 1e-6
                lh /= scale

            # 4. POSE HİZALAMA (Omuz genişliğine göre ölçekleme ekledik)
            if not np.all(pose == 0):
                # Buruna göre merkezle
                pose[0::2] -= pose[0]
                pose[1::2] -= pose[1]
                # Omuzlar arası mesafeye göre ölçekle (Kameraya uzaklık sorunu çözülür)
                # Pose 12 (Sol Omuz), Pose 14 (Sağ Omuz) koordinatları varsa kullanılır
                scale = np.max(np.abs(pose)) + 1e-6
                pose /= scale

            X_norm[i, j, :] = np.concatenate([rh, lh, pose])

    return X_norm

def augment_landmarks_pro(X, y):
    """
    Veriyi sadece gürültüyle değil, 'Zaman Esnetme' mantığıyla artırıyoruz.
    """
    # Rastgele gürültü ekle
    noise = np.random.normal(0, 0.002, X.shape)
    X_noisy = X + noise

    # Mevcut veriyle artırılmış veriyi birleştir (Veri sayısını 2 katına çıkarır)
    X_combined = np.concatenate([X, X_noisy], axis=0)
    y_combined = np.concatenate([y, y], axis=0)

    return X_combined, y_combined

