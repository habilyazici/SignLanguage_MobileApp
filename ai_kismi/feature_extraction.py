def safe_collect_data(df, folder_name, save_name):
    """
    Verileri parça parça işler ve kaldığı yerden devam eder.
    """
    X_final = []
    y_final = []

    # Geçici dosyaların yolu
    temp_path_X = os.path.join(SAVE_PATH, f"temp_X_{save_name}.npy")
    temp_path_y = os.path.join(SAVE_PATH, f"temp_y_{save_name}.npy")

    # 1. Eğer önceden kaydedilmiş veri varsa yükle (Resume)
    if os.path.exists(temp_path_X):
        X_final = list(np.load(temp_path_X))
        y_final = list(np.load(temp_path_y))
        print(f"🔄 {save_name} için kayıt bulundu! {len(X_final)} videodan devam ediliyor...")

    start_index = len(X_final)
    total = len(df)

    for index in range(start_index, total):
        row = df.iloc[index]
        video_name = row['video_id'] + "_color.mp4"
        video_path = os.path.join(DATA_PATH, folder_name, video_name)

        if os.path.exists(video_path):
            landmarks = process_video(video_path)
            X_final.append(landmarks)
            y_final.append(row['label'])
        else:
            # Video yoksa boş veri ekleyelim ki sıra bozulmasın (Padding gibi)
            X_final.append(np.zeros((60, 106)))
            y_final.append(row['label'])
            print(f"⚠️ Eksik video: {video_name}")

        # Her 100 videoda bir "Hayat Kurtaran" yedekleme yap
        if (index + 1) % 100 == 0 or (index + 1) == total:
            np.save(temp_path_X, np.array(X_final))
            np.save(temp_path_y, np.array(y_final))
            print(f"💾 Yedeklendi: {folder_name} -> {index + 1}/{total}")

    # İşlem tamamen bittiğinde geçici dosyayı nihai dosyaya çevir
    final_X_path = os.path.join(SAVE_PATH, f"X_{save_name}.npy")
    final_y_path = os.path.join(SAVE_PATH, f"y_{save_name}.npy")

    np.save(final_X_path, np.array(X_final))
    np.save(final_y_path, np.array(y_final))

    print(f"✅ {save_name} TAMAMLANDI ve nihai dosya oluşturuldu.")
    return np.array(X_final), np.array(y_final)

