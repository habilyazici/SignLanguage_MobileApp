def process_video(video_path, sequence_length=60):
    """
    Videoyu açar, her kareyi işler ve sabit uzunlukta bir dizi döndürür.
    """
    cap = cv2.VideoCapture(video_path)
    window_landmarks = []

    # 1. Videodaki her kareyi işle
    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break

        # Görüntüyü işle
        image = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        results = holistic.process(image)

        # Koordinatları süz ve listeye ekle
        landmarks = extract_landmarks(results)
        window_landmarks.append(landmarks)

    cap.release()

    # 2. Kare Sayısını Sabitleme (Padding veya Sampling)
    # Eğer video kısa ise son kareyi tekrar et (Padding)
    if len(window_landmarks) < sequence_length:
        diff = sequence_length - len(window_landmarks)
        last_frame = window_landmarks[-1] if len(window_landmarks) > 0 else np.zeros(106)
        for _ in range(diff):
            window_landmarks.append(last_frame)

    # Eğer video uzun ise aralardan seçerek 60 kareye düşür (Sampling)
    elif len(window_landmarks) > sequence_length:
        indices = np.linspace(0, len(window_landmarks) - 1, sequence_length, dtype=int)
        window_landmarks = [window_landmarks[i] for i in indices]

    return np.array(window_landmarks)

