def extract_landmarks(results):
    """
    MediaPipe sonuçlarından sağ el, sol el ve pose noktalarını süzüp
    tek bir liste (106 sayı) haline getirir.
    """
    # Sağ El (21 nokta * 2 koordinat = 42)
    rh = np.array([[res.x, res.y] for res in results.right_hand_landmarks.landmark]).flatten() if results.right_hand_landmarks else np.zeros(21*2)

    # Sol El (21 nokta * 2 koordinat = 42)
    lh = np.array([[res.x, res.y] for res in results.left_hand_landmarks.landmark]).flatten() if results.left_hand_landmarks else np.zeros(21*2)

    # Pose / Üst Vücut (Seçili 11 nokta * 2 koordinat = 22)
    # vücut hatlarını takip etmek için omuz, dirsek, bilek, göz ve burun noktalarını alıyoruz.
    if results.pose_landmarks:
        # MediaPipe Pose'da toplam 33 nokta vardır, biz sadece kritik olanları seçiyoruz
        # Seçilenler: 0, 2, 5, 7, 8, 11, 12, 13, 14, 15, 16 (Yüz ve Üst Gövde)
        pose_selected_indices = [0, 2, 5, 7, 8, 11, 12, 13, 14, 15, 16]
        pose = np.array([[results.pose_landmarks.landmark[i].x, results.pose_landmarks.landmark[i].y] for i in pose_selected_indices]).flatten()
    else:
        pose = np.zeros(11*2) #

    # Hepsini birleştir: 42 + 42 + 22 = 106
    return np.concatenate([rh, lh, pose])

